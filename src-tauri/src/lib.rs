/// Creates a new `AsyncCommand` for a given program name.
/// On Windows, it adds the `CREATE_NO_WINDOW` flag to prevent console windows from appearing.
fn create_command(name: &str) -> AsyncCommand {
    let mut cmd = AsyncCommand::new(name);
    #[cfg(windows)]
    {
        use std::os::windows::process::CommandExt;
        const CREATE_NO_WINDOW: u32 = 0x08000000;
        cmd.creation_flags(CREATE_NO_WINDOW);
    }
    cmd
}

/// Custom serializer for `u64` to `String` for JSON compatibility.
fn u64_to_string<S>(x: &u64, s: S) -> Result<S::Ok, S::Error>
where
    S: Serializer,
{
    s.serialize_str(&x.to_string())
}

/// Custom deserializer for `String` to `u64` for JSON compatibility.
fn string_to_u64<'de, D>(deserializer: D) -> Result<u64, D::Error>
where
    D: Deserializer<'de>,
{
    String::deserialize(deserializer)?.parse::<u64>().map_err(serde::de::Error::custom)
}

/// Represents a simple metadata tag, equivalent to the frontend's `Tag` interface.
#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct SimpleTag {
    /// The name of the tag (e.g., "ARTIST").
    #[serde(rename = "Name")]
    name: String,
    /// The value of the tag (e.g., "The Beatles").
    #[serde(rename = "String")]
    string: String,
    /// The language of the tag value, following IETF BCP 47.
    #[serde(rename = "TagLanguageIETF", default)]
    language: Option<String>,
}

/// Represents a track within the media file, equivalent to the frontend's `Track` interface.
#[derive(Debug, Serialize, Deserialize, Default, Clone)]
pub struct Track {
    /// A display-friendly name generated by the UI (e.g., "Video #1").
    display_name: String,
    /// The codec of the track (e.g., "V_MPEG4/ISO/AVC").
    codec: String,
    /// The track ID from the container.
    #[serde(default)]
    id: u64,
    /// The optional name of the track from metadata.
    track_name: Option<String>,
    /// The unique track identifier (UID).
    #[serde(serialize_with = "u64_to_string", deserialize_with = "string_to_u64")]
    uid: u64,
    /// A list of tags associated with this track.
    tags: Vec<SimpleTag>,
}

/// Represents the entire structure of tags from a file, equivalent to the frontend's `FileData` interface.
#[derive(Debug, Serialize, Deserialize, Default)]
pub struct FileData {
    /// Tags that apply to the file as a whole.
    global_tags: Vec<SimpleTag>,
    /// A list of tracks contained within the file.
    tracks: Vec<Track>,
}

/// Internal struct for deserializing track properties from `mkvmerge -J` output.
#[derive(Debug, Deserialize)]
struct MkvMergeTrackProperties {
    #[serde(default)]
    track_name: Option<String>,
    uid: u64,
}

/// Internal struct for deserializing a single track from `mkvmerge -J` output.
#[derive(Debug, Deserialize)]
struct MkvMergeTrack {
    codec: String,
    id: u64,
    properties: MkvMergeTrackProperties,
    #[serde(rename = "type")]
    track_type: String,
}

/// Internal struct for deserializing the root JSON object from `mkvmerge -J` output.
#[derive(Debug, Deserialize)]
struct MkvMergeOutput {
    tracks: Vec<MkvMergeTrack>,
}

/// Internal struct for deserializing the `<Targets>` element from `mkvextract` XML output.
#[derive(Debug, Deserialize)]
struct XmlTargets {
    #[serde(rename = "TrackUID", default)]
    track_uid: Vec<u64>,
}

/// Internal struct for deserializing a `<Tag>` element from `mkvextract` XML output.
#[derive(Debug, Deserialize)]
struct XmlTag {
    #[serde(rename = "Targets")]
    targets: XmlTargets,
    #[serde(rename = "Simple", default)]
    simple: Vec<SimpleTag>,
}

/// Internal struct for deserializing the root `<Tags>` element from `mkvextract` XML output.
#[derive(Debug, Deserialize)]
struct XmlTags {
    #[serde(rename = "Tag", default)]
    tag: Vec<XmlTag>,
}


