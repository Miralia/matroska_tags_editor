/// Opens a native file dialog to allow the user to select a Matroska file.
#[tauri::command]
async fn open_file_dialog(app: tauri::AppHandle) -> Option<String> {
    let (tx, rx) = oneshot::channel();
    app.dialog()
        .file()
        .add_filter("Matroska", &["mkv", "mk3d", "mka", "mks"])
        .pick_file(move |file_path| {
            let result = file_path.map(|p| p.to_string());
            tx.send(result).unwrap();
        });
    rx.await.unwrap_or(None)
}

/// Extracts all track and tag metadata from a given Matroska file.
///
/// This function orchestrates calls to two external command-line tools from the MKVToolNix suite:
/// 1. `mkvmerge -J`: To get detailed information about all tracks in the file (ID, UID, codec, etc.).
/// 2. `mkvextract tags`: To extract all global and per-track tags into an XML format.
///
/// It then parses and combines the output from both tools into a single `FileData` struct
/// that is sent to the frontend.
///
/// # Arguments
///
/// * `path` - The absolute path to the Matroska file.
///
/// # Errors
///
/// This function will return an error if `mkvmerge` or `mkvextract` cannot be executed,
/// if they exit with a non-zero status code, or if their output cannot be parsed.
#[tauri::command]
async fn get_file_data(path: String) -> Result<FileData, String> {
    log::info!("Getting file data for: {}", path);
    let mkvmerge_output = create_command("mkvmerge")
        .arg("-J")
        .arg(&path)
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .output()
        .await
        .map_err(|e| e.to_string())?;

    if !mkvmerge_output.status.success() {
        let error_message = String::from_utf8_lossy(&mkvmerge_output.stderr).to_string();
        log::error!("mkvmerge command failed: {}", error_message);
        return Err(error_message);
    }

    let mkvmerge_stdout = String::from_utf8_lossy(&mkvmerge_output.stdout);
    log::debug!("mkvmerge stdout: {}", mkvmerge_stdout);

    let mkvmerge_data: MkvMergeOutput = serde_json::from_str(&mkvmerge_stdout)
        .map_err(|e| {
            let error_message = format!("Failed to parse mkvmerge JSON: {}", e);
            log::error!("{}", error_message);
            error_message
        })?;

    let mut video_count = 1;
    let mut audio_count = 1;
    let mut subtitle_count = 1;

    let mut tracks: Vec<Track> = mkvmerge_data.tracks.into_iter().map(|t| {
        let display_name = match t.track_type.as_str() {
            "video" => {
                let name = format!("Video#{}", video_count);
                video_count += 1;
                name
            }
            "audio" => {
                let name = format!("Audio#{}", audio_count);
                audio_count += 1;
                name
            }
            "subtitles" => {
                let name = format!("Subtitle#{}", subtitle_count);
                subtitle_count += 1;
                name
            }
            _ => "Unknown".to_string(),
        };

        Track {
            display_name,
            codec: t.codec,
            id: t.id,
            track_name: t.properties.track_name,
            uid: t.properties.uid,
            tags: Vec::new(),
        }
    }).collect();

    let mut global_tags = Vec::new();
    let mkvextract_output = create_command("mkvextract")
        .arg("tags")
        .arg(&path)
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .output()
        .await
        .map_err(|e| e.to_string())?;

    if mkvextract_output.status.success() {
        let mkvextract_stdout = String::from_utf8_lossy(&mkvextract_output.stdout);
        log::debug!("mkvextract stdout (all tags): {}", mkvextract_stdout);
        if let Ok(xml_data) = quick_xml::de::from_str::<XmlTags>(&mkvextract_stdout) {
            for tag in xml_data.tag {
                if tag.targets.track_uid.is_empty() {
                    global_tags.extend(tag.simple);
                } else {
                    for uid in tag.targets.track_uid {
                        if let Some(track) = tracks.iter_mut().find(|t| t.uid == uid) {
                            track.tags.extend(tag.simple.clone());
                        }
                    }
                }
            }
        }
    } else {
        log::warn!("Could not extract tags, maybe none exist.");
    }

    tracks.sort_by_key(|t| t.id);

    Ok(FileData {
        global_tags,
        tracks,
    })
}

/// Internal struct for serializing a `<Simple>` tag to XML.
#[derive(Debug, Serialize)]
struct XmlWriteSimpleTag<'a> {
    #[serde(rename = "Name")]
    name: &'a str,
    #[serde(rename = "String")]
    string: &'a str,
}

/// Internal struct for serializing a `<Targets>` element to XML.
#[derive(Debug, Serialize, Default)]
struct XmlWriteTargets {
    #[serde(rename = "TrackUID", default, skip_serializing_if = "Vec::is_empty")]
    track_uid: Vec<u64>,
}

/// Internal struct for serializing a `<Tag>` element to XML.
#[derive(Debug, Serialize)]
struct XmlWriteTag<'a> {
    #[serde(rename = "Targets")]
    targets: XmlWriteTargets,
    #[serde(rename = "Simple")]
    simple: Vec<XmlWriteSimpleTag<'a>>,
}

/// Internal struct for serializing the root `<Tags>` element to XML.
#[derive(Debug, Serialize)]
#[serde(rename = "Tags")]
struct XmlWriteTags<'a> {
    #[serde(rename = "Tag")]
    tag: Vec<XmlWriteTag<'a>>,
}

/// Converts the application's `FileData` struct into an XML string
/// compatible with `mkvpropedit`.
fn to_xml(data: &FileData) -> Result<String, quick_xml::se::SeError> {
    let mut xml_tags = Vec::new();

    if !data.global_tags.is_empty() {
        xml_tags.push(XmlWriteTag {
            targets: XmlWriteTargets::default(),
            simple: data
                .global_tags
                .iter()
                .map(|t| XmlWriteSimpleTag {
                    name: &t.name,
                    string: &t.string,
                })
                .collect(),
        });
    }

    for track in &data.tracks {
        if !track.tags.is_empty() {
            xml_tags.push(XmlWriteTag {
                targets: XmlWriteTargets {
                    track_uid: vec![track.uid],
                },
                simple: track
                    .tags
                    .iter()
                    .map(|t| XmlWriteSimpleTag {
                        name: &t.name,
                        string: &t.string,
                    })
                    .collect(),
            });
        }
    }

    let tags_to_serialize = XmlWriteTags { tag: xml_tags };
    quick_xml::se::to_string(&tags_to_serialize)
}


/// Saves the provided tag data back to the specified Matroska file.
///
/// This command receives the complete tag data as a JSON string from the frontend.
/// It then:
/// 1. Deserializes the JSON into the `FileData` struct.
/// 2. Serializes the `FileData` struct into a temporary XML file.
/// 3. Executes `mkvpropedit` to apply the tags from the XML file to the Matroska file.
///
/// # Arguments
///
/// * `path` - The absolute path to the Matroska file to be modified.
/// * `json_data` - A JSON string representing the `FileData` to be saved.
///
/// # Errors
///
/// This function will return an error if any step in the process fails, including
/// JSON parsing, XML generation, temporary file creation, or the `mkvpropedit` command execution.
#[tauri::command]
async fn save_file_data(path: String, json_data: String) -> Result<(), String> {
    log::info!("[BACKEND] Received save request for path: {}", path);
    log::debug!("[BACKEND] Raw JSON data received:\n{}", json_data);

    let file_data: FileData = serde_json::from_str(&json_data)
        .map_err(|e| format!("Failed to parse JSON data: {}", e))?;

    let xml_string =
        to_xml(&file_data).map_err(|e| format!("Failed to generate XML: {}", e))?;

    log::debug!("Generated XML:\n{}", xml_string);

    let mut temp_file = tempfile::Builder::new()
        .suffix(".xml")
        .tempfile()
        .map_err(|e| format!("Failed to create temporary file: {}", e))?;

    use std::io::Write;
    temp_file.write_all(xml_string.as_bytes())
        .map_err(|e| format!("Failed to write to temporary file: {}", e))?;

    let temp_path = temp_file.path().to_str().ok_or("Temporary file path is not valid UTF-8")?;

    log::info!("Running mkvpropedit on {}", path);
    let output = create_command("mkvpropedit")
        .arg(&path)
        .arg("--tags")
        .arg(format!("all:{}", temp_path))
        .output()
        .await
        .map_err(|e| format!("Failed to execute mkvpropedit: {}", e))?;

    if !output.status.success() {
        let error_message = String::from_utf8_lossy(&output.stderr);
        log::error!("mkvpropedit failed: {}", error_message);
        return Err(format!("mkvpropedit failed: {}", error_message));
    }

    log::info!("Successfully saved tags to {}", path);
    Ok(())
}


/// A command to forcefully close the application.
/// This is called by the frontend after a user confirms closing with unsaved changes.
#[tauri::command]
fn force_close(app: tauri::AppHandle) {
    app.exit(0);
}

/// The main entry point for the Tauri application logic.
/// This function sets up the Tauri application builder, registers plugins,
/// defines invoke handlers, and runs the application.
#[cfg_attr(mobile, tauri::mobile_entry_point)]
pub fn run() {
    tauri::Builder::default()
        .plugin(tauri_plugin_opener::init())
        .plugin(tauri_plugin_dialog::init())
        .plugin(tauri_plugin_fs::init())
        .plugin(tauri_plugin_shell::init())
        .plugin(tauri_plugin_process::init())
        .plugin(
            tauri_plugin_log::Builder::new()
                .targets([
                    Target::new(TargetKind::Stdout),
                    Target::new(TargetKind::LogDir { file_name: None }),
                    Target::new(TargetKind::Webview),
                ])
                .build(),
        )
        .setup(|app| {
            let app_handle = app.handle().clone();
            tauri::async_runtime::spawn(async move {
                let commands = ["mkvmerge", "mkvextract", "mkvpropedit"];
                let checks = commands.iter().map(|&command| async move {
                    log::info!("Checking for command: {}", command);
                    let check_command = if cfg!(target_os = "windows") { "where" } else { "which" };
                    let status = create_command(check_command).arg(command).output().await;

                    match status {
                        Ok(output) if output.status.success() => {
                            log::info!("Command found: {}", command);
                            None
                        }
                        _ => {
                            log::warn!("Command not found: {}", command);
                            Some(command.to_string())
                        }
                    }
                });

                let results: Vec<Option<String>> = future::join_all(checks).await;
                let not_found_commands: Vec<String> = results.into_iter().flatten().collect();

                if !not_found_commands.is_empty() {
                    let message = format!(
                        "The following required commands were not found:\n\n{}\n\nPlease install MKVToolNix and ensure it is in your system's PATH.",
                        not_found_commands.join("\n")
                    );

                    let dialog = app_handle.dialog().clone();
                    dialog
                        .message(message)
                        .title("Environment Check Failed")
                        .show(move |_| {
                            app_handle.exit(1);
                        });
                }
            });
            Ok(())
        })
        .invoke_handler(tauri::generate_handler![
            greet,
            open_file_dialog,
            get_file_data,
            save_file_data,
            force_close,
            drop::create_drop_window,
            drop::hide_drop_window,
            drop::show_drop_window,
            drop::take_drop_files
        ])
        .on_window_event(|window, event| {
            if let tauri::WindowEvent::CloseRequested { api, .. } = event {
                api.prevent_close();
                let window = window.clone();
                window.emit("close-requested", {}).unwrap();
            }
        })
        .run(tauri::generate_context!())
        .expect("error while running tauri application");
}